// Helper to get file type from extension
const getFileType = (filename: string): string => {
  const ext = filename.split('.').pop()?.toLowerCase();
  const typeMap: Record<string, string> = {
    pdf: 'application/pdf',
    docx: 'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
    xlsx: 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet',
    pptx: 'application/vnd.openxmlformats-officedocument.presentationml.presentation',
    csv: 'text/csv',
    png: 'image/png',
    jpg: 'image/jpeg',
    mp4: 'video/mp4',
    zip: 'application/zip',
  };
  return typeMap[ext || ''] || 'application/octet-stream';
};

// Helper to generate realistic file sizes
const getFileSize = (filename: string): number => {
  const ext = filename.split('.').pop()?.toLowerCase();
  const sizeRanges: Record<string, [number, number]> = {
    pdf: [500000, 3000000],
    docx: [200000, 1500000],
    xlsx: [150000, 2000000],
    pptx: [2000000, 8000000],
    csv: [50000, 500000],
    png: [500000, 5000000],
    jpg: [300000, 3000000],
    mp4: [10000000, 50000000],
    zip: [5000000, 20000000],
  };
  const [min, max] = sizeRanges[ext || ''] || [100000, 1000000];
  return Math.floor(Math.random() * (max - min) + min);
};

